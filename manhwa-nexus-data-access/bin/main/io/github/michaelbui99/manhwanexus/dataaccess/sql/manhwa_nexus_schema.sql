-- Types
CREATE TYPE SOURCE_MATERIAL AS ENUM ('ORIGINAL', 'LIGHT_NOVEL', 'WEB_NOVEL', 'VIDEO_GAME', 'OTHER', 'VISUAL_NOVEL');

